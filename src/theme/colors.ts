/**
 *  https://css-tricks.com/converting-color-spaces-in-javascript/
 */

const hexToRGBA = (h: string, alpha = 1) => {
    let r = '0',
        g = '0',
        b = '0'

    // 3 digits
    if (h.length == 4) {
        r = '0x' + h[1] + h[1]
        g = '0x' + h[2] + h[2]
        b = '0x' + h[3] + h[3]

        // 6 digits
    } else if (h.length == 7) {
        r = '0x' + h[1] + h[2]
        g = '0x' + h[3] + h[4]
        b = '0x' + h[5] + h[6]
    }

    return 'rgb(' + +r + ',' + +g + ',' + +b + ',' + alpha / 100 + ')'
}

export const generateColorTheme = (colors: {
    surface: string
    onSurface: string

    primary: string
    onPrimary: string

    secondary: string
    onSecondary: string

    accent: string
    onAccent: string

    error: string
    onError: string

    warning: string
    onWarning: string

    notice: string
    onNotice: string

    success: string
    onSuccess: string
}): Colors => {
    const result = {}

    // Generate all the opacity level 5 - 95%
    for (const color in colors) {
        const value = colors[color]

        result[color] = value // 100%

        for (let i = 5; i < 100; i = i + 5) {
            result[color + i] = hexToRGBA(value, i)
        }
    }

    return result as Colors
}

